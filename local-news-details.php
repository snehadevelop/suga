                        <p class="fpw5 ">Release – The Turks and Caicos Islands Tourist Board successfully wrapped up its 2022 Turks and Caicos Conference (TACC).
                        </p>
                        <p class="fpw5 ln32 pb-2">
                            The 18th annual TACC held from Wednesday, October 26 to Friday, October 28invited the destination’s leading wholesalers and online travel agents to the Turks and Caicos Islands for site inspections, updates on the Turks and Caicos Islands, as well as for networking and conducting meetings.</p>
                        <p class="fpw5 ln32 pb-2">The three-day conference began with an opening ceremony at the Beaches Key West Village – where attendees enjoyed food and entertainment. Remarks were provided by the Minister of Tourism, Hon. Josephine Connolly, Acting Director of Tourism, Mary Lightbourne, President of the Turks and Caicos Hotel and Tourism Association (TCHTA), Trevor Musgrove, and the vote of thanks was delivered by the TCI Tourist Board’s Senior Public Relations Officer, Gabriel Saunders.</p>
                        <p class="fpw5 ln32 pb-2">Site inspections occurred the following day and provided wholesalers with an opportunity to better understand the various properties and rooms they were selling. These occurred at Beach Enclave Long Bay, the Ritz-Carlton, Wymara Resorts & Villas, Rock House, Sailrock Resort, The Meridian Club on Pine Cay, The Shore Club, as well as at The Sands. Later, guests attended Fish Fry to experience the culture of the Turks and Caicos Islands at the popular Thursday night event.</p>
                        <p class="fpw5 ln32 pb-2">On the final day of TACC, hosted at Blue Haven Resort, wholesalers delivered presentations to hoteliers, followed by industry overviews from Minister of Tourism, Hon. Connolly and Acting Director of Tourism, Mary Lightbourne. After the presentations, attendees enjoyed local cuisine in a Blue Haven Penthouse – providing spectacular views of Providenciales. Post-lunch, wholesalers, airline representatives, and hoteliers reconvened for a tradeshow – facilitating further conversations and networking amongst key industry partners. TCI Tourist Board’s Product Development Officer, Candesha Mills, closed the event with a vote of thanks – and TACC formally concluded that evening with a dinner at The Palms.</p>
                        <p class="fpw5 ln32 pb-2">“It was an honour to be able to host our top industry partners for the 2022 Turks and Caicos Conference and once again allow them to touch, taste, and experience the Turks and Caicos Islands”, said Minister of Tourism, Hon. Josephine Connolly. “The sharing of knowledge and best practices, as well as the networking between our industry partners at TACC, play a massive role in the success of the Turks and Caicos Islands’ tourism industry. We greatly appreciate our guests for their attendance”, added Hon. Connolly.</p>
                        <p class="fpw5 ln32 pb-2">
                            “We are incredibly proud with the results of our 18th annual Turks and Caicos Conference – the first TACC that has been hosted fully in-person since 2019”, stated Acting Director of Tourism Mary Lightbourne.</p>



